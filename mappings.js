// ── processField ─────────────────────────────────────────
//  Aplica la regla correspondiente a un campo PDF.
//  Normaliza el valor antes de buscar en tablas de equiv
//  para tolerar acentos y variaciones menores del PDF.

function processField(pdfKey, value) {
  if (!value || value.trim() === '') return value;

  const rule = FIELD_RULES[pdfKey];
  if (!rule) return value;

  // Sin transformación
  if (rule.raw) return value.trim();

  // Limpieza de texto
  if (rule.clean && rule.clean !== 'visa') {
    const cleaned = CLEAN[rule.clean](value);
    // Los campos con translate se procesan en popup.js después
    return cleaned;
  }

  if (rule.clean === 'visa') return CLEAN.visa(value);

  // Equivalencia — normaliza para tolerancia de acentos
  if (rule.equiv) {
    const map = EQUIV[rule.equiv];
    const normalized = (value || '')
      .trim()
      .normalize('NFD')
      .replace(/[\u0300-\u036f]/g, '')
      .replace(/[^\x00-\x7F]/g, '')
      .toUpperCase()
      .replace(/\s+/g, ' ')
      .trim();

    // Buscar por valor normalizado primero, luego por valor original
    const byNormalized = Object.entries(map).find(([k]) => {
      const kn = k.normalize('NFD')
        .replace(/[\u0300-\u036f]/g, '')
        .replace(/[^\x00-\x7F]/g, '')
        .toUpperCase()
        .replace(/\s+/g, ' ')
        .trim();
      return kn === normalized;
    });

    if (byNormalized) return byNormalized[1];

    // Fallback directo
    const direct = map[value.trim()];
    if (direct) return direct;

    console.warn(`[VP] Sin equivalencia en "${rule.equiv}" para: "${value}"`);
    return value;
  }

  return value;
}


// ── getTranslatableFields ────────────────────────────────
//  Devuelve un objeto { pdfKey: valorLimpio } solo con los
//  campos marcados como translate:true y que tengan valor.

function getTranslatableFields(rawFields) {
  const result = {};
  for (const [key, rule] of Object.entries(FIELD_RULES)) {
    if (!rule.translate) continue;
    const val = (rawFields[key] || '').trim();
    if (!val) continue;
    result[key] = val;
  }
  return result;
}
