const CLEAN = {

  // Quita acentos, caracteres no-ASCII y especiales. Solo letras, números y espacios. MAYÚSCULAS.
  text: (val) => (val || '')
    .trim()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^\x00-\x7F]/g, '')
    .replace(/[^A-Z0-9 ]/gi, '')
    .replace(/\s+/g, ' ')
    .trim()
    .toUpperCase(),

  // Solo minúsculas — único campo que NO va a mayúsculas
  email: (val) => (val || '')
    .trim()
    .toLowerCase(),

  // Solo dígitos, máximo 10
  phone: (val) => (val || '')
    .trim()
    .replace(/[^\d]/g, '')
    .slice(0, 10),

  // Solo dígitos, máximo 5
  zip: (val) => (val || '')
    .trim()
    .replace(/[^\d]/g, '')
    .slice(0, 5),

  // Solo los últimos 8 dígitos (de derecha a izquierda)
  visa: (val) => (val || '')
    .trim()
    .replace(/[^\d]/g, '')
    .slice(-8),
};


